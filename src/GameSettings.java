/**
 * Main class for the Connect Four game colour components.
 * Handles ANSI Color Codes for the Terminal
 */
public class GameSettings {
    public static final String RESET = "\u001B[0m";
	public static final String RED = "\u001B[31m";    // For Player R
	public static final String YELLOW = "\u001B[33m"; // For Player Y
	public static final String CYAN = "\u001B[36m";   // For UI Elements
	public static final String GREEN = "\u001B[32m";  // For Wins/Success
}